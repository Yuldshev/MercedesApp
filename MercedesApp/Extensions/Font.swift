import SwiftUI

extension Font {
  static func corporateARegular(size: CGFloat) -> Font {
    return Font.custom("CorporateAPro-Regular", size: size)
  }
  
  static func corporateAMedium(size: CGFloat) -> Font {
    return Font.custom("CorporateAPro-Medium", size: size)
  }
  
  static func arialRegular(size: CGFloat) -> Font {
    return Font.custom("Arial-Regular", size: size)
  }
  
  static func arialMedium(size: CGFloat) -> Font {
    return Font.custom("Arial-Medium", size: size)
  }
  
  static func arialBold(size: CGFloat) -> Font {
    return Font.custom("Arial-Bold", size: size)
  }
}
